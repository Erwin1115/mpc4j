package edu.alibaba.mpc4j.common.tool.bristol;

/**
 * gate operation.
 *
 * @author Weiran Liu
 * @date 2025/4/7
 */
public enum GateOperation {
    /**
     * INV gate
     */
    INV,
    /**
     * NOT gate
     */
    NOT,
    /**
     * Equal gate
     */
    EQ,
    /**
     * Equal Wire gate
     */
    EQW,
    /**
     * XOR gate
     */
    XOR,
    /**
     * AND gate
     */
    AND,
    /**
     * Multiple AND gate
     */
    MAND,
}
